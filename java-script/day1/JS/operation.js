// 연산
// 더하기, 빼기, 곱하기, 나누기
1 + 1;
1 - 1;
1 * 1;
1 / 1;
// % 연산자 - 값을 나눈 나머지를 리턴
2 % 2; // 0
3 % 2; // 1

// 비교 연산 - boolean 으로 리턴
// 비교 연산자 1
//  = = : 두 값이 동일한지 비교, ! = : 두 값이 동일하지 않은지 비교
console.log(1 == 1)
console.log(1 == 2)

console.log(1 != 1)
console.log(1 != 2)

// 비교 연산자 2
// = = = : 두 변수의 값과 자료형이 모두 동일한지 비교, ! = = : 두 변수의 값과 자료형이 모두 동일하지 않은지 비교
console.log("1" == 1)
console.log("1" === 1)

// 논리 연산
// 논리 연산자
// || : or, && : and
// or 연산자 = 여러가지 조건중 하나만 만족해도 true
console.log(("1" == 1) || ("1" === 1) || false)

// and 연산자 = 여러가지 조건을 모두 만족해야 true
console.log("1" == 1 && "1" === 1 && true)

